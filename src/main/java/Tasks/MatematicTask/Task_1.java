package Tasks.MatematicTask;
/*
Найти сумму, произведение и разность двух чисел
 */
public class Task_1 {
    public static void main(String[] args) {

        int a = 6;
        int b = 3;

        int sumAB = a + b;
        int productAB = a * b;
        int differenceAB = a / b;

        System.out.println("Сумма чисел a и b равна: " + sumAB + "\n" +
                            "Произведение чисел a и b равно: " + productAB + "\n" +
                            "Разность чисел a и b равна: " +  differenceAB);

	/*
	"\n" - это новая строка;
	*/
    }
}
